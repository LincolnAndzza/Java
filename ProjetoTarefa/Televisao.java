public class Televisao{ 

    private int volume;     
    private int canal; 

    public void setVolume(int volume){         
        this.volume = volume;     
    }     

    public void setCanal(int canal){          
        this.canal = canal;     
    } 

    public int getVolume(){         
       return this.volume;     
    } 
  
    public int getCanal(){         
        return this.canal;     
    } 
} 


